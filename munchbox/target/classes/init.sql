-- Tabela USUARIO
CREATE TABLE USUARIO (
                         ID_USUARIO BIGSERIAL PRIMARY KEY,
                         LOGIN VARCHAR(50) NOT NULL,
                         SENHA VARCHAR(50) NOT NULL,
                         DATA_ATUALIZACAO TIMESTAMP NOT NULL,
                         DATA_INCLUSAO TIMESTAMP NOT NULL
